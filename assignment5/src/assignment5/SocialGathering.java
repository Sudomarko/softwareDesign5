package assignment5;

public class SocialGathering {
	public String event;
	public String date;
	
	public SocialGathering(String event, String date) {
		this.event = event;
		this.date = date;
	}
	
	public String getEvent() {
		return this.event;
	}
	
	public String getDate() {
		return this.date;
	}
}
